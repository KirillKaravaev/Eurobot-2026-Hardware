/*Фазо-импульсное преобразование (Phase-Pulse Conversion - PPC)*/
typedef volatile struct{
	uint16_t cntr_reg;					/*Значение, записываемое в регистр CNTR_REG компаратора перезапуска таймера
	 	 	 	 	 	 	 	 	 	CNTR на ПЛИС*/

	uint16_t flt_reg;					/*Значение, записываемое в регистр FLT_REG компаратора предельной задержки
	 	 	 	 	 	 	 	 	 	данных с ППСД. Когда таймер CNTR досчитает до этого значения, на МК поступит
	 	 	 	 	 	 	 	 	 	сигнал перерывания вне зависимости от того, получены данные с ППСД или нет*/

	uint32_t freq;						/*Системная частота sys_clk на входе таймера ПЛИС*/

	float mul;							/*Множитель для перевода интервалов дискретизации в градусы. Может быть вычислен
		 	 	 	 	 	 	 	 	по формуле mul = 360 * 50 * tir_reg / freq , где freq / (50*tir_reg) -
		 	 	 	 	 	 	 	 	число импульсов дискретизации на период 50 Гц синусоиды */

} PPC_Param;
/*Возвращаемые ФИП значения*/
typedef volatile struct{
	uint16_t tir_reg;					/*Значение, записываемое в регистр T_xx_REG компаратора для выдачи управляющего
	 	 	 	 	 	 	 	 	 	импульса на тиристор 1, включенный между 2 фазами */

	bool tir_del;						/*Значение, записываемое в регистр T_xx_DEL для задержки выдачи управляющего
										импульса на тиристор 1. 1 - нужна задержка, 0 - нет*/

	int32_t temp;						/*Переменная для отладки*/

} PPC_Out;




