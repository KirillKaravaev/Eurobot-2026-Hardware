typedef struct{
	double In_Coef[MAX_ORDER+1];		/*Коэффициенты фильтра для входного сигнала (A). Являются весовыми
										коэффициентами при рассчете текущего значения отфильтрованного
										сигнала в зависимости от order значений предыдущих входных сигналов,
										а также текущего значения входного сигнала (поэтому их MAX_ORDER+1)
										-- интерполируем текущий сигнал, зная предыдущие значения*/

	double Out_Coef[MAX_ORDER+1];		/*Коэффициенты фильтра для выходного сигнала (B). Являются весовыми
										коэффициентами при рассчете текущего значения отфильтрованного
										сигнала в зависимости от order значений предыдущих выходных сигналов,
										(поэтому их MAX_ORDER, но еще добавляем дополнительный единичный элемент
										с индексом ноль Out_Coef[0] = 1)*/

	size_t Order;						/*Порядок фильтра. Он задает число коэффициентов фильтра, а также
										число значащих элементов в массивах input_buff и output_buff*/
} Filter_Coefficients;



typedef volatile struct{
	float In_Buf[MAX_ORDER];			/*Предыдущие значения входного сигнала. Необходимы для расчетов
										текущего выходного сигнала. Тип uint - так как выходной сигнал
										фильтра все равно идет на DAC, который работает только с uint*/

	float Out_Buf[MAX_ORDER];			/*Предыдущие значения выходного сигнала. Необходимы для расчетов
										текущего выходного сигнала*/
} Filter_Buffers;


/*ПАРАМЕТРЫ ФИЛЬТРАЦИИ определяют физические свойства фильтра. Задаются пользователем.
 */
typedef volatile struct{
	uint8_t Type;						/*Тип фильтра - пока цифровой код*/

	size_t Order;						/*Порядок фильтра. Он задает число коэффициентов фильтра, а также
										число значащих элементов в массивах input_buff и output_buff*/

	uint32_t Low_Freq;					/*нижняя граница частоты пропускания/отсечки (в зависимости от типа
										фильтра)*/

	uint32_t High_Freq;					/*верхняя граница частоты пропускания/отсечки (в зависимости от типа
										фильтра)*/


	uint32_t Sampling_Freq;				/*Частота дискретизации. Определяет, как часто АЦП оцифровывает
	 	 	 	 	 	 	 	 	 	сигнал*/

	uint8_t Swing_Range;				/*Размах колебаний АЧХ в процентах (НО НЕ БОЛЬШЕ 29!!!)
	 	 	 	 	 	 	 	 	 	Для фильтра Баттерворта значение равно 0*/

	uint32_t Band_Width;				/*Ширина переходной зоны фильтра - определяется как диапазон,
										разделяющий область с близким к единице коэффициентом передачи,
										и область, в которой коэффициент передачи близок к нулю */

	float Out_Offset;					/*Смещение выходного сигнала В ВОЛЬТАХ, необходимо для корректного отображения
		 	 	 	 	 	 	 	 	сигнала при помощи ЦАП, который не оперирует с отрицательными значениями*/

} Filter_Parameters;




/*Параметры ПИД-регулятора*/
