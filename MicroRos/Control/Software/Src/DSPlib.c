/**
  * @brief  Раcчет коэффициентов режекторного узкополосного фильтра 2 порядка (NBRF - narrow-band rejection filter)(19.3 стр 370)
  *
  *	@param	Filtering_Options		-- Структура, содержащая параметры фильтрации - нижнюю и верхнюю границы
  * 								   полосы пропускания/отсечки, ширину переходной зоны и частоту дискретизации
  * @retval Filter_Parameters		-- Структура, содержащая параметры фильтра - весовые коэффициенты,
  * 								   порядок изначения прошлых входных и выходных сигналов.
  *
  *             АЧХ
  *
  *           BW
  *	  |     |<-->|
  *	  |_____.    .     ______
  *	 a|		\    .    /
  *	 m|		 \   .   /
  *	 p| 	  \  .  /
  *	  |        \ . /
  *   |_________\./___________
  *			   rej_f			freq
  *
  */
Filter_Coefficients NBRF_calculate_coef(Filter_Parameters *par){ //Передаем по адресу, так как хотим менять значения. Поэтому оператор ->
	Filter_Coefficients coef = {0};	//А здесь уже локальная структура, потому операция .

	double rej_f = (par->High_Freq == 0) ? par->Low_Freq : par->High_Freq; //Задаем любую одну, либо обе
	double disc_f = par->Sampling_Freq;
	double BW = par->Band_Width;

	double f = rej_f/disc_f;							//относительная частота фильтруемой гармоники
	double bw = BW/disc_f;
	double R = 1 - 3*bw;
	double K = (1 - 2*R*cos(2*PI*f) + R*R) / (2 - 2*cos(2*PI*f));



	coef.In_Coef[0] = K;  						//A0
	coef.In_Coef[1] = -2*K*cos(2*PI*f);			//A1
	coef.In_Coef[2] = K;							//A2
	coef.Out_Coef[0] = 1;						//B0
	coef.Out_Coef[1] = 2*R*cos(2*PI*f);			//B1
	coef.Out_Coef[2] = - R*R;					//B2
	coef.Order = 2;
	return coef;
}




