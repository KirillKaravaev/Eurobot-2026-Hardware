/**
  * @brief  Функция вычисления действующего (среднеквадратичного, RMS) значения для синусоидального сигнала
  *
  * @note	Данная функция постоянно вычисляет действующее значение (измеренное, например, АЦП), но
  * 		обновляет его с частотой примерно в 2 раза меньшей частоты сигнала
  * 		и должна быть расположена в бесконечном цикле while с проверкой флага обновления
  * 		данных, прерывании по обновлении данных и т.п.
  *
  * @param	input_sig				-- Входной сигнал, чаще всего с АЦП
  *
  * @retval sine_RMS				-- Выходное среднеквадратичное (RMS, действующее) значение
  *
  */
int32_t Sine_RMS(int32_t input_sig){
	static int32_t Max = 0;
	static int32_t Min = pow(2,DAC_BIT_RATE);
	static float sine_RMS;
	static uint32_t cntr = 666;
//Ищем максимальное значение синусоиды,
	if(input_sig > Max){
		Max = input_sig;
		cntr = cntr + 2;
	}
//Ищем минимальное значение синусоиды
	else if(input_sig < Min){
		Min = input_sig;
		cntr = cntr + 2;
	}
//Если оба значения найдены, то декрементируем счетчик, пока он не достагнет нуля. При этом
//так как увеличивался счетчик на + 2, а уменьшается на 1, то частота обновления sine_RMS
//будет в 2 раза меньше частоты сигнала (примерно, так как есть еще дополнительная поправка
//при перезадании счетчика. (по умолчанию 1000). Чем больше отношение частоты дискретизации
// к частоте сигнала, тем больше необходимо выбирать эту поправку. В идеале она должна быть
//не меньше, чем отношение частоты дискретизации к частоте сигнала, в противном случае могут
//возникать ситуации, когда среднее вычисляется раньше, чем найден максимум или минимум,(например,
//первое измерение попало близ вершины синусоиды на участок с отрицательной производной. Тогда
//в цикле мы, пока не дойдем до низа, будем все время уменьшать счетчик без его увеличения, тем
//самым попадем раньше в рассчет действующего значения, чем найдем минимальное значение)

//TODO избавиться от поправки к значению cntr;
	else {
		cntr--;
	}
//Если счетчик равен нулю, то пересчитываем действующее значение, а затем переинициализируем значения, тем самым
//перезапуская поиск максимума и минимума.
	if(cntr == 0){
		sine_RMS = (float)(Max-Min) / SQRT2;
		cntr = 666;
		Max = 0;
		Min = pow(2,DAC_BIT_RATE);
	}

	return (int32_t)sine_RMS;
}




