/**
  * @brief  Функция вычисления cкользящего среднего значения
  *
  * @note	Алгоритм аналогичен фильтрации, но с постоянными коэффициентами фильтра 1/pnt_num, и в соответсвие
  * 		с этим упрощением оптимизирован. Располагать данную функцию необходимо там же, где и функцию фильтрации-
  * 		в бесконечном цикле while с проверкой флага обновления данных, прерывании по обновлении данных и т.п.
  *
  *	@note	Количество точек, по которому проводится усреднение, задается параметром AVERAGING_NUM, объявленным в
  *			заголовочном файле
  *
  * @param	input_sig				-- Входной сигнал, который необходимо усреднить. Чаще всего сигнал с АЦП
  *
  *	@param	averaging_num			-- Число значений, по которым проводится усреднение (Но не больше MAX_AVERAGING_NUM)
  *
  *	@param	Rolling_Avg_State		-- Структура, содержащая состояния блока и сохраняемые значения, необходимые для расчета
  *
  * @retval int32_t					-- Выходное скользящее (текущее) среднее значение
  *
  */
int32_t Rolling_Avg(int32_t input_sig, uint16_t averaging_num, Rolling_Avg_State *avg_st){
//Сохраняем предыдущие входные значения, необходимые для вычисления скользящего среднего в In_Buff[]
//Накапливаем сумму входных значений.для оптимизации вычислений не вычисляем накопленную в In_Buff[] сумму каждый
//раз заново, а замещаем последнее оставшееся значение новым входным)
	averaging_num = (averaging_num < MAX_AVERAGING_NUM) ? averaging_num : MAX_AVERAGING_NUM;
	avg_st->_sum += input_sig - avg_st->_In_Buff[averaging_num-1];
//Сдвигаем значения в массиве In_Buff для сохранения нового (самое старое значение, находящееся в конце массива, затирается)
	for(int i = averaging_num-1; i > 0; i--){
		avg_st->_In_Buff[i] = avg_st->_In_Buff[i-1];
	}
	avg_st->_In_Buff[0] = input_sig;
//Раз в RESET_SUM вызовов обнуляем ошибку, накопленную при вычислении sum, просто пересчитывая значения по данным в In_Buff[]

#ifdef RESET_SUM
	avg_st->_callback_num++;
	if(avg_st->_callback_num > RESET_SUM){
		avg_st->_callback_num = 0;
		avg_st->_sum = 0;
		for(int i = 0; i < averaging_num; i++){
			avg_st->_sum += avg_st->_In_Buff[i];
		}
	}
#endif
	return (int32_t)(avg_st->_sum / averaging_num);
}




/**
  * @brief  Функция вычисления cкользящего среднего значения для аргумента типа float
  *
  * @note	Алгоритм аналогичен фильтрации, но с постоянными коэффициентами фильтра 1/pnt_num, и в соответсвие
  * 		с этим упрощением оптимизирован. Располагать данную функцию необходимо там же, где и функцию фильтрации-
  * 		в бесконечном цикле while с проверкой флага обновления данных, прерывании по обновлении данных и т.п.
  *
  *	@note	Количество точек, по которому проводится усреднение, задается параметром AVERAGING_NUM, объявленным в
  *			заголовочном файле
  *
  * @param	input_sig				-- Входной сигнал, который необходимо усреднить. Чаще всего нормированный сигнал с АЦП
  *
  *	@param	averaging_num			-- Число значений, по которым проводится усреднение (Но не больше MAX_AVERAGING_NUM)
  *
  *	@param	Rolling_Avg_Param		-- Структура, содержащая параметры блока и сохраняемые значения, необходимые для расчета
  *
  * @retval int32_t					-- Выходное скользящее (текущее) среднее значение
  *
  */
float Rolling_Avg_f(float input_sig, uint16_t averaging_num, Rolling_Avg_Statef *avg_st){
//Сохраняем предыдущие входные значения, необходимые для вычисления скользящего среднего в In_Buff[]
//Накапливаем сумму входных значений.для оптимизации вычислений не вычисляем накопленную в In_Buff[] сумму каждый
//раз заново, а замещаем последнее оставшееся значение новым входным)
#ifdef USE_MALLOC_TO_ALLOCATE_ARRAYS_MEMORY

#else
	averaging_num = (averaging_num < MAX_AVERAGING_NUM) ? averaging_num : MAX_AVERAGING_NUM;
#endif
	int n = avg_st->_n;

//Обнуляем счетчик вызовов функции, тем самым обновляя индексацию циклического буфера с прошлыми измерениями
	if(n == averaging_num){
		avg_st->_n = 0;
		n = 0;
	}
//Прибавляем новое значение и вычитаем старое, находящееся в буфере по индексу n.
	avg_st->_sum = avg_st->_sum + input_sig - avg_st->_In_Buff[n];
/*После того, как мы использовали старое значение, можем его затереть новым. При этом мы постепенно двигаемся
по массиву и затираем старые значения новыми, а по достижении конца переходим в начало, тем самым
мы можем не смещать все элементы массива, как это было сделано ранее. результат в обоих случаях одинаковый.
Только в раннем варианте новое значение мы добавляли строго в начало массива, тем самым смещая все старые,
а здесь новое значение является головой буфера и само перемещается, а справа от него находится хвост,
тем самым мы как бы движемся по кольцу
Классический случай: {[head]..........[tail]} -> {[head+1][head]..........[tail+1]}  [tail] затерли сдвигом всех элементов}
Кольцевой буфер:     {.......[head][tail]...} -> {........[head][head+1][tail+1]..}  [tail] затерли не сдвигом, а [head+1]}*/
	avg_st->_In_Buff[n] = input_sig;
//Увеличиваем индекс, тем самым при следующей итерации будем замещать следующее значение в буфере
	(avg_st->_n) ++;
	return avg_st->_sum / (float)averaging_num;
}
