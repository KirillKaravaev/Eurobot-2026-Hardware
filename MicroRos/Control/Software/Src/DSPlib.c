/**
  * @brief  Функция окончательного расчета (нормирования) коэффициентов High-Pass фильтра Чебышева-Баттерворта
  * 		порядка n (n КРАТНО 2!!)
  *
  *	@param	Filtering_Options		-- Структура, содержащая параметры фильтрации - нижнюю и верхнюю границы
  * 								   полосы пропускания/отсечки, ширину переходной зоны и частоту дискретизации
  * @retval Filter_Parameters		-- Структура, содержащая параметры фильтра - весовые коэффициенты,
  * 								   порядок и значения прошлых входных и выходных сигналов. Возвращает
  * 								   НОРМИРОВАННЫЕ коэффициенты фильтра порядка n.
  *
  *	   		         АЧХ
  *	  |
  *	  | 	 	              ______
  *	 a|			             /
  *	 m|		 	 	        /
  *	 p| 	  	           /
  *   |             	  /
  *   |_________________./___________
  *			           High_Freq     freq
  *
  */
Filter_Coefficients HighPassFilter_calculate_coef(Filter_Parameters *par){
//Проверяем, что введенный порядок фильтра четный и принадлежит от 2 до MAX_ORDER
	CheckOrder(par, MAX_ORDER);
	Filter_Coefficients coef = CBFn_calculate_coeff(par, HIGH_PASS);
//Нормируем полученные коэффициенты (своя нормировка для каждого типа фильтра)
	double In_Coef_Sum = 0;
	double Out_Coef_Sum = 1;   //1, так как Out_Coef[0] = -1, а в рассчетах его не учитывают
		for(int i = 0; i <= par->Order ; i++){
			In_Coef_Sum += coef.In_Coef[i] * pow(-1,i);
			Out_Coef_Sum += coef.Out_Coef[i] * pow(-1,i);
		}
	double mul = In_Coef_Sum / (1 - Out_Coef_Sum);
	for(int i = 0; i <= par->Order; i++){
		coef.In_Coef[i] /= mul;
	}
	coef.Order = par->Order;
	return coef;
}




