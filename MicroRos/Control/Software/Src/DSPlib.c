/**
  * @brief  Функция прямого преобразования Парка для двухфазных токов (Поворот двумерной системы координат на угол fi,
  * 		чаще всего служит для перехода от неподвижной СК статора электродвигателя к подвижной СК ротора)
  * 		(https://www.matematicus.ru/vysshaya-matematika/analiticheskaya-geometriya-na-ploskosti/povorot-osej-koordinat)
  *
  * @note   Обратите внимание, в разных источниках преобразование Парка рассматривается либо как просто поворот системы координат,
  * 		либо как последовательное применение преобразования Кларка и последующего поворота СК (в матричном виде-
  * 		произведение матрицы поворота на матрицу преобразования Кларка Kp = Kr*Kc). В данном случае преобразование
  * 		Парка - просто поворот двумерной СК на угол fi
  *
  * @param	Init_Coord_Sys			-- Структура, содержащая компоненты двумерного вектора в проекции на исходную
  * 								   (неподвижной) систему координат и угол поворота результирующей системы
  * 								   относительно исходной
  *
  * @retval Res_Coord_Sys			-- Структура, содержащая компоненты того же двумерного вектора, но уже в проекции
  * 								   на повернутую (подвижную) систему координат
  *
  *      Y\    y|
  *        \    |
  *         \ fi|
  *          \<-|
  *           \ |        X
  *            \|________
  *                      x
  *
  */
Res_Coord_Sys Direct_Park_Transform(Init_Coord_Sys Init_CS){
	Res_Coord_Sys Res_CS = {0};
//	float TR_Mtx[2][2] = { 	{cos(Init_CS.fi), sin(Init_CS.fi)},
//							{-sin(Init_CS.fi), cos(Init_CS.fi)}	};
	Res_CS.X = 	 Init_CS.x * cos(Init_CS.fi) + Init_CS.y * sin(Init_CS.fi);
	Res_CS.Y = - Init_CS.x * sin(Init_CS.fi) + Init_CS.y * cos(Init_CS.fi);
	return Res_CS;
}




/**
  * @brief  Функция обратного преобразования Парка для двухфазных токов (Поворот двумерной системы координат на
  * 		угол Fi, чаще всего служит для перехода от подвижной СК ротора электродвигателя к неподвижной СК статора)
  * 		(https://www.matematicus.ru/vysshaya-matematika/analiticheskaya-geometriya-na-ploskosti/povorot-osej-koordinat)
  *
  * @note   Обратите внимание, в разных источниках обратное преобразование Парка рассматривается либо как просто
  * 		поворот системы координат, либо как последовательный поворот СК и применение обратного преобразования Кларка
  * 		(в матричном виде - произведение матрицы обратного преобразования Кларка на матрицу поворота : Kp^-1 =
  * 		(Kr*Kc)^-1 = Kc^-1 * Kr^-1 , где Kc^-1 и Kr^-1 должны быть дополнены до квадратных матриц, либо последовательно
  * 		(слева-направо) перемножены на двумерный вектор-столбец входных данных). В данном случае обратное преобразование
  * 		Парка - просто поворот двумерной СК на угол Fi
  *
  *
  * @param	Res_Coord_Sys			-- Структура, содержащая компоненты двумерного вектора в проекции на повернутую
  * 								   (подвижную) систему координат и угол "обратного" поворота исходной системы
  * 								   относительно результирующей
  *
  * @retval Init_Coord_Sys			-- Структура, содержащая компоненты того же двумерного вектора, но уже в проекции
  * 								   на исходную (неподвижную) систему координат
  *
  *      Y\    y|
  *        \    |
  *         \ fi|
  *          \<-|
  *           \ |        X
  *            \|________
  *                      x
  *
  */
Init_Coord_Sys Reverse_Park_Transform(Res_Coord_Sys Res_CS){
	Init_Coord_Sys Init_CS = {0};
	Init_CS.x = Res_CS.X * cos(Res_CS.Fi) - Res_CS.Y * sin(Res_CS.Fi);
	Init_CS.y = Res_CS.X * sin(Res_CS.Fi) + Res_CS.Y * cos(Res_CS.Fi);
	return Init_CS;
}




/**
  * @brief  Функция прямого преобразования Кларка для мощности в сбалансированной системе Ix+Iy+Iz = 0 (Переход от
  * 		трехфазных токов к двухфазным)
  * 		(Подробный вывод - https://en.wikipedia.org/wiki/Direct-quadrature-zero_transformation, в главе Power-variant form - формулы,
  * 		Также формулы в главе Simplified transformation - https://en.wikipedia.org/wiki/Alpha–beta_transformation,
  * 		Зачем это нужно, а также графический вывод формул - Рис 9 https://habr.com/ru/companies/npf_vektor/articles/367653/)
  *
  * @param	Init_Coord_Sys			-- Структура, содержащая "оси" исходной системы трехфазных токов
  *
  * @retval Res_Coord_Sys			-- Структура, содержащая "оси" результирующей системы двухфазных токов
  *
  *
  *         y\                        Y|
  *           \                        |
  *            \ ________x     =>      |__________X
  *            /
  *           /
  *         z/
  */
Res_Coord_Sys Direct_Clarke_Transform(Init_Coord_Sys CS_3D){
//Simplified transformation - https://en.wikipedia.org/wiki/Alpha–beta_transformation
	Res_Coord_Sys CS_2D;
	CS_2D.X = 	(2./3.) * ( CS_3D.x - 0.5 * CS_3D.y - 0.5 * CS_3D.z ); //из сбалансированности системы Ix+Iy+Iz = 0 следует, что CS_2D.X = CS_3D.x
	CS_2D.Y =  (2./3.) * ( SQRT3/2 * CS_3D.y - SQRT3/2 * CS_3D.z);
	CS_2D.Z = 0;
	return CS_2D;
}




/**
  * @brief  Функция обратного преобразования Кларка для мощности в сбалансированной системе Ix+Iy+Iz = 0 (Переход от
  * 		двухфазных токов к трехфазным)
  * 		(Подробный вывод - https://en.wikipedia.org/wiki/Direct-quadrature-zero_transformation, в главе Power-variant form - формулы,
  * 		Также формулы в главе Simplified transformation - https://en.wikipedia.org/wiki/Alpha–beta_transformation,
  * 		Зачем это нужно, а также графический вывод формул - Рис 9 https://habr.com/ru/companies/npf_vektor/articles/367653/)
  *
  * @param	Res_Coord_Sys			-- Структура, содержащая "оси" системы двухфазных токов
  *
  * @retval Init_Coord_Sys			-- Структура, содержащая "оси" системы трехфазных токов
  *
  *
  *        Y|                         y\
  *         |                           \
  *         |_________X        =>        \ ________x
  *                                      /
  *                                     /
  *                                   z/
  */
Init_Coord_Sys Reverse_Clarke_Transform(Res_Coord_Sys CS_2D){
//Simplified transformation - https://en.wikipedia.org/wiki/Alpha–beta_transformation
	Init_Coord_Sys CS_3D;
	CS_3D.x = CS_2D.X;
	CS_3D.y = - 0.5 * CS_2D.X + SQRT3/2 * CS_2D.Y;
	CS_3D.z = - 0.5 * CS_2D.X - SQRT3/2 * CS_2D.Y;
	return CS_3D;
}




