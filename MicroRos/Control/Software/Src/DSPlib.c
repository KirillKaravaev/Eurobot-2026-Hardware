/**
  * @brief  Функция вычисления действующего (среднеквадратичного, RMS) значения для произвольного дискретного сигнала
  * 		(https://en.wikipedia.org/wiki/Root_mean_square)
  *
  * @note	Если в процессе работы программы необходимо изменить rms_num, то ОБЯЗАТЕЛЬНО должна быть вызвана функция RMS_rst_err,
  * 		причем строго после изменения rms_num и до вызова функции RMS с новым значением rms_num. Это необходимо, так как RMS
  * 		вычисляет накопленную сумму единожды при старте, а далее  использует уже рассчитанное значение без проверок. В итоге
  * 		при уменьшении rms_num сумма останется та же, а делитель уменьшится. При увеличении rms_num такой проблемы нет.
  *
  * @note	Данная функция динамически вычисляет среднеквадратичное значение (измеренное, например, АЦП) по rms_num < MAX_RMS_NUM
  * 		дискретным отсчетам. Выходное значение пересчитывается по поступлении каждого нового отчета. Аналогична
  * 		функции усреднения, но здесь усредняется не сама величина, а ее квадрат, с последующим извлечением корня.
  * 		Вызывать данную функцию необходимо постоянно по поступлении нового значения с АЦП или т.п.
  *
  *	@note	Параметр MAX_RMS_NUM объявлен в заголовочном файле. Обратите внимание, для корректного вычисления среднеквадратичного
  *			значения периодического сигнала параметр rms_num должен быть не меньше, чем число считываний на периоде этого
  *			сигнала
  *
  * @param	input_sig				-- Входной дискретный сигнал
  *
  * @param	rms_num				    -- Количество значений, по которым вычисляется среднеквадратичное. Должно быть кратно периоду сигнала
  *                                    Может принимать значения от 0 до величины MAX_RMS_NUM, заданной в DSPlib.h
  *
  * @param	RMS_State               -- Структура, хранящая прошлые отчеты - накопленную сумму, а также массив всех предыдущих значений
  *
  * @retval RMS						-- Выходное среднеквадратичное (RMS, действующее) значение
  *
  */
int32_t RMS(int32_t input_sig, uint32_t rms_num, RMS_State *rms_st){
	rms_num = (rms_num <= MAX_RMS_NUM) ? rms_num : MAX_RMS_NUM;
	int32_t input_sig_2 = input_sig * input_sig;
	int n = rms_st->_n;
//Если число вызовов функции достигло rms_num, то сбрасываем счетчик, тем самым новые значения массива будут затирать старые
	if(n == rms_num){
		rms_st->_n = 0;
		n = 0;
	}
//Накапливаем сумму квадратов входных значений. Для оптимизации вычислений не вычисляем накопленную в In_Buff_2[]
//сумму каждый раз заново, а замещаем последнее оставшееся значение новым входным)
	rms_st->_sum_2 = rms_st->_sum_2 + input_sig_2 - rms_st->_In_Buff_2[n];
//Перезаписываем квадрат измеренного значения
	rms_st->_In_Buff_2[n] = input_sig_2;

// Увеличиваем счетчик числа вызовов функции
	(rms_st->_n) ++;
	return (int32_t)( sqrt(rms_st->_sum_2 / rms_num));
}




